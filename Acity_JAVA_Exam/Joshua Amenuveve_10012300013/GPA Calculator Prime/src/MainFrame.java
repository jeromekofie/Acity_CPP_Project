    private void CalculateGPAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateGPAActionPerformed

    String name = tfName.getText().trim();
    String studentId = tfID.getText().trim();
    String course = Course.getText().trim();
    String save = SaveMethod.getSelectedItem().toString();

if (name.isEmpty() || studentId.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Enter Student Name and ID.");
    return;
}

try {
    String num = JOptionPane.showInputDialog("Enter number of Sub Courses:");
    if (num == null) return;
    int Subcourses = Integer.parseInt(num);

    double totalGradePoints = 0.0;
    double totalCreditHours = 0.0;

    String all = "";

    for (int i = 1; i <= Subcourses; i++) {
        String grade = JOptionPane.showInputDialog("Course" + i + " Grade:");
        if (grade == null) return;
        grade = grade.toUpperCase().trim();

        double gradePoint = -1;
        if (grade.equals("A")) {
            gradePoint = 4.0;
        } else if (grade.equals("B+")) {
            gradePoint = 3.5;
        } else if (grade.equals("B")) {
            gradePoint = 3.0;
        } else if (grade.equals("C+")) {
            gradePoint = 2.5;
        } else if (grade.equals("C")) {
            gradePoint = 2.0;
        } else if (grade.equals("D")) {
            gradePoint = 1.5;
        } else if (grade.equals("E")) {
            gradePoint = 1.0;
        } else if (grade.equals("F")) {
            gradePoint = 0.0;
        } else {
            JOptionPane.showMessageDialog(null, "Invalid grade: " + grade);
            return;
        }

        String credits = JOptionPane.showInputDialog("Enter Credit hours: " + i + ":");
        if (credits == null) return;
        double credit = Double.parseDouble(credits);

        totalGradePoints += gradePoint * credit;
        totalCreditHours += credit;

        all += "Sub Course " + i + ": Grade : " + grade + ", Grade Point: " + gradePoint + ", Credit Hours: " + credit + "\n";
    }

    double gpa = totalGradePoints / totalCreditHours;

    String finalOutput =  "              Calulated GPA                  " + "\n"
            + "Course: " + course + "\n"
            + all
            + "GPA: " + String.format("%.2f", gpa) + "\n"
            + "Name: " + name + "\n"
            + "ID: " + studentId + "\n";

    JOptionPane.showMessageDialog(null, finalOutput);

    Object[] rowData = {
            
            name,
            studentId,
            course,
            totalCreditHours,
            gpa,

    };

    DefaultTableModel model = (DefaultTableModel) tableStudents.getModel();
    model.addRow(rowData);

    if (save.equalsIgnoreCase("File")) {
        FileWriter writer = new FileWriter("StudentGPA.txt", true);
        writer.write(finalOutput);
        writer.close();
    } else if (save.equalsIgnoreCase("Database")) {
        JOptionPane.showMessageDialog(null, "Database: Under Constructiion");
    } 

} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(null, "Invalid number entered. Please try again.");
} catch (IOException ex) {
    JOptionPane.showMessageDialog(null, "Error writing to file: " + ex.getMessage());
}


    }//GEN-LAST:event_CalculateGPAActionPerformed

