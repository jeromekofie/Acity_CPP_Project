    private double calculategpa(String grade, int creditHours) {
    double gradePoint = switch (grade.toUpperCase()) {
        case "A" -> 4.0;
        case "B+" -> 3.5;
        case "B" -> 3.0;
        case "C+" -> 2.5;
        case "C" -> 2.0;
        case "D" -> 1.5;
        case "E" -> 1.0;
        case "F" -> 0.0;
        default -> -1; // Error
    };
    return (gradePoint * creditHours) / creditHours;
}


