    private void calculategpabuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculategpabuttonActionPerformed
        try{ 
        String grade = gradestxt.getText();
        int credit = Integer.parseInt(credithourstxt.getText());
        
        double totalPoints = calculategpa(grade, credit);
        
        double gpa = totalPoints / credit;
        
        gpadisplayarea.setText(String.format("%.2f", gpa));
        
        } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "enter a valid number for credit hours");
        }
    }//GEN-LAST:event_calculategpabuttonActionPerformed

    private void savebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebuttonActionPerformed
        String name = nametxt.getText();
        String id = idtxt.getText();
        String course = coursetxt.getText();
        String grade = gradestxt.getText();
        
    try {
          int credit = Integer.parseInt(credithourstxt.getText());
          double totalPoints = calculategpa(grade, credit);
          double gpa = totalPoints / credit;
          
          if(fileordatabasecombobox.getSelectedItem().toString().equalsIgnoreCase("File")) {
           FileWriter fw = new FileWriter("gparecords.txt", true);
          fw.write(name + "\n" + id + "\n" + course + "\n" + credit + "\n" + grade + "\n" + gpa + "\n");
          fw.close();
          JOptionPane.showMessageDialog(this, "saved to file!");
        } else{
          JOptionPane.showMessageDialog(this, "Database: Under Construction");
          }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Enter valid numbers for credit hours.");
}           catch (IOException e) {
    JOptionPane.showMessageDialog(this, "File saving error.");
}
    }//GEN-LAST:event_savebuttonActionPerformed

