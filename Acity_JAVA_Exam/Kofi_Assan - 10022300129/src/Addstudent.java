    private double getGradeValue(String grade) throws Exception {
        switch (grade.toUpperCase()) {
            case "A": 
                return 4.0;
            case "B+": 
                return 3.5;
            case "B": 
                return 3.0;
            case "C+": 
                return 2.5;
            case "C": 
                return 2.0;
            case "D": 
                return 1.5;
            case "E": 
                return 1.0;
            case "F": 
                return 0.0;
            default:
                
                return Double.parseDouble(grade);
        }
    }

    private void saveToFile(String studentId, String studentName, String gpa, String Course1, String Course2, String Course3, String credhours1, String credhours2, String credhours3) {
        try {
            File file = new File("gpa_records.txt");
            FileWriter writer = new FileWriter(file, true); 
            try (BufferedWriter buffered = new BufferedWriter(writer)) {
                String record = String.format("%s|%s|%s|%s|%s|%s|%s|%s|%s%n",
                        studentId,
                        studentName, 
                        Course1,
                        Course2,
                        Course3,
                        credhours1,
                        credhours2,
                        credhours3,
                        gpa,

                        new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new java.util.Date()));
                
                buffered.write(record);
            }
            
            JOptionPane.showMessageDialog(this, "GPA record saved to file successfully!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving GPA record: " + e.getMessage());
        }
    }
