    private void calc_gpaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calc_gpaActionPerformed
        String grade1Str = jTextField5.getText().trim();
        String grade2Str = jTextField10.getText().trim();
        String grade3Str = jTextField11.getText().trim();
        
        String hours1Str = jTextField1.getText().trim();
        String hours2Str = jTextField8.getText().trim();
        String hours3Str = jTextField9.getText().trim();

        double grade1Value = 0, grade2Value = 0, grade3Value = 0;
        double hours1 = 0, hours2 = 0, hours3 = 0;
        try {
            grade1Value = getGradeValue(grade1Str);
            grade2Value = getGradeValue(grade2Str);
            grade3Value = getGradeValue(grade3Str);
            hours1 = Double.parseDouble(hours1Str);
            hours2 = Double.parseDouble(hours2Str);
            hours3 = Double.parseDouble(hours3Str);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Invalid input!!! Please check grades and credit hours.");
            return;
        }

        double totalPoints = grade1Value * hours1 + grade2Value * hours2 + grade3Value * hours3;
        double totalHours = hours1 + hours2 + hours3;
        if (totalHours == 0) {
            JOptionPane.showMessageDialog(this, "Total credit hours cannot be zero.");
            return;
        }
        double gpa = totalPoints / totalHours;
        String gpaString = String.format("%.2f", gpa);
        
        
        String studentId = jTextField2.getText().trim();
        String studentName = jTextField4.getText().trim();
        String Course1 = jTextField3.getText().trim();
        String Course2 = jTextField6.getText().trim();
        String Course3 = jTextField7.getText().trim();
        String credhours1 = jTextField1.getText().trim();
        String credhours2 = jTextField8.getText().trim();
        String credhours3 = jTextField9.getText().trim();

        
        
        if (jComboBox2.getSelectedItem().equals("File")) {
            saveToFile(studentId, studentName, Course1, Course2, Course3, credhours1, credhours2, credhours3, gpaString);
        }else if(jComboBox2.getSelectedItem().equals("Database")){
            JOptionPane.showMessageDialog(null, "Under Construction");
        }
        
        JOptionPane.showMessageDialog(this, "Your GPA is: " + gpaString);
    }//GEN-LAST:event_calc_gpaActionPerformed

