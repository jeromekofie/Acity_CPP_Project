    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
      String name = tfName.getText();
      String studentId = tfID.getText();
      String save = (String) cmbSave.getSelectedItem();
      
      String course_1 = tfC1.getText();
      String course_2 = tfC2.getText();
      String course_3 = tfC3.getText();
      
      int credit_hours1 = Integer.parseInt(tfcred1.getText());
      int credit_hours2 = Integer.parseInt(tfcred2.getText());
      int credit_hours3 = Integer.parseInt(tfcred3.getText());
      
      String grade_1 = tfGrade1.getText();
      String grade_2 = tfGrade2.getText();
      String grade_3 = tfGrade3.getText();
      
      
      double grade_point1 = 0;
      double grade_point2 = 0;
      double grade_point3 = 0;
      
      
      
      switch(grade_1){
          case "A" -> grade_point1 = 4.0;
          case "B+" -> grade_point1 = 3.5;
           case "B" -> grade_point1 = 3.0;
           case "C+" -> grade_point1 = 2.5;
           case "C" -> grade_point1 = 2.0;
           case "D" -> grade_point1 = 1.5;
            case "E" -> grade_point1 = 1.0;
            case "F" -> grade_point1 = 0.0;        
      }
      
        switch(grade_2){
          case "A" -> grade_point2 = 4.0;
          case "B+" -> grade_point2 = 3.5;
           case "B" -> grade_point2 = 3.0;
           case "C+" -> grade_point2 = 2.5;
           case "C" -> grade_point2= 2.0;
           case "D" -> grade_point2 = 1.5;
            case "E" -> grade_point2 = 1.0;
            case "F" -> grade_point2 = 0.0;        
      }
          switch(grade_3){
          case "A" -> grade_point3 = 4.0;
          case "B+" -> grade_point3 = 3.5;
           case "B" -> grade_point3 = 3.0;
           case "C+" -> grade_point3 = 2.5;
           case "C" -> grade_point3 = 2.0;
           case "D" -> grade_point3 = 1.5;
            case "E" -> grade_point3 = 1.0;
            case "F" -> grade_point3 = 0.0;        
      }
           
          double sumCreditHours = credit_hours1 + credit_hours2 + credit_hours3;
          
          double sumGrades = (grade_point1*credit_hours1)+  (grade_point2*credit_hours1)+ (grade_point3*credit_hours1);
          
          
          double gpa = sumGrades/sumCreditHours;
          
          
          if("Database".equals(save)){
              
              JOptionPane.showMessageDialog(null,"Database: Under Construction");
          }
          
          else if("File".equals(save)){
              
          try {
              FileWriter writer = null;
          
              writer = new FileWriter ("gpa_records.txt",true);
              
              writer.write("\n **STUDENT RECORDS** \n" );
              writer.write("Name: " + name +"\n");
              writer.write("Student ID: " + studentId +"\n");

              writer.write("Course 1: " + course_1 +"\n");
              writer.write("Credit Hours:" + credit_hours1 + "\n");
              writer.write("Grade:" + grade_1 +"\n");

              writer.write("Course 2: " + course_2 +"\n");
              writer.write("Credit Hours:" + credit_hours2 + "\n");
              writer.write("Grade:" + grade_2 +"\n");

              writer.write("Course 3: " + course_3 +"\n");
              writer.write("Credit Hours:" + credit_hours3 + "\n");
              writer.write("Grade:" + grade_3 +"\n");
              writer.write("GPA " + gpa + "\n");
              
              writer.write("***************************");

              writer.close();
              
              JOptionPane.showMessageDialog(null, "Record written to file successfuly");
          } catch (IOException ex) {
              Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
          }
          }
          
    }//GEN-LAST:event_btnCalculateActionPerformed

