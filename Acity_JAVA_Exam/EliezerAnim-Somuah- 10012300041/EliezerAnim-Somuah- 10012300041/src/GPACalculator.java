    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        if (courseList.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please add at least one course before calculating GPA.");
            return;
        }
        
        double totalPoints = 0;
        int totalCredits = 0;
        
        for (Course course : courseList) {
            double gradePoints = convertGradeToPoints(course.grade);
            totalPoints += gradePoints * course.credits;
            totalCredits += course.credits;
        }
        
        if (totalCredits > 0) {
            double gpa = totalPoints / totalCredits;
            lbDisplayGPA.setText(String.format("%.2f", gpa));
        } else {
            lbDisplayGPA.setText("0.00");
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

    private double convertGradeToPoints(double grade) {
        if (grade >= 90) return 4.0;
        else if (grade >= 85) return 3.7;
        else if (grade >= 80) return 3.3;
        else if (grade >= 75) return 3.0;
        else if (grade >= 70) return 2.7;
        else if (grade >= 65) return 2.3;
        else if (grade >= 60) return 2.0;
        else if (grade >= 55) return 1.7;
        else if (grade >= 50) return 1.3;
        else if (grade >= 45) return 1.0;
        else return 0.0;
    }

    private void bntAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntAddCourseActionPerformed
        try {
            String name = tfCourseName.getText().trim();
            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter a course name.");
                return;
            }
            
            double grade = Double.parseDouble(tfGrade.getText());
            if (grade < 0 || grade > 100) {
                JOptionPane.showMessageDialog(this, "Grade must be between 0 and 100.");
                return;
            }
            
            int credits = Integer.parseInt(tfCreditHours.getText());
            if (credits <= 0) {
                JOptionPane.showMessageDialog(this, "Credit hours must be greater than 0.");
                return;
            }
    
            Course course = new Course(name, grade, credits);
            courseList.add(course);
    
            model.addRow(new Object[] {name, String.format("%.1f", grade), credits});
            
            // Clear input fields
            tfCourseName.setText("");
            tfGrade.setText("");
            tfCreditHours.setText("");
    
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numbers for grade and credits.");
        }
    }//GEN-LAST:event_bntAddCourseActionPerformed

