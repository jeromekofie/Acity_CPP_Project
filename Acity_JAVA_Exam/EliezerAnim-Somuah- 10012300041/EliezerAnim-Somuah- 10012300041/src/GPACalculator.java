    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
            String selectedOption = cmbSaveOption.getSelectedItem().toString();
            String name = tfStudentName.getText();
            String id = tfStudentID.getText();
            String gpa = lbDisplayGPA.getText();
            
            
            if (selectedOption.equals("Save to File")) {
                try (BufferedWriter writer = new BufferedWriter(new FileWriter("gpa_records.txt", true))) {
                    if (name.isEmpty() || id.isEmpty() ){
                        JOptionPane.showMessageDialog(this,
                        "Please enter all fields",
                        "Try again",
                        JOptionPane.ERROR_MESSAGE);
                    }else{
                    writer.write(name + "\n");
                    writer.write(id + "\n");
                    }
                    for (Course course : courseList) {
                        writer.write("Course: " + course.name + ", Grade: " + course.grade + ", Credits: " + course.credits + "\n");
                    }
                    writer.write(gpa + "\n");
                    writer.write("---------------\n");
                    JOptionPane.showMessageDialog(this, "Saved successfully to file.");
                    
                    
                } catch (IOException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error saving to file.");
                }
        
            } else if (selectedOption.equals("Save to Database")) {
                JOptionPane.showMessageDialog(this, "Under construction.");
            }
    }//GEN-LAST:event_btnSaveActionPerformed

