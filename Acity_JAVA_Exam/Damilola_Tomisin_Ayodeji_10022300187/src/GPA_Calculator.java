    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
      String name = tfName.getText();
    String id   = tfId.getText();
    if (name.isEmpty() || id.isEmpty()) {
        JOptionPane.showMessageDialog(this,"Please enter both Name and ID.", "Input Error",
         JOptionPane.ERROR_MESSAGE
        );
        return;
    }

    String option = (String) cbSelect.getSelectedItem();
    if ("Save to Database".equals(option)) {
        JOptionPane.showMessageDialog(this,"Save to database is currently under construction.","Info",
            JOptionPane.INFORMATION_MESSAGE
        );
        return;
    }
    try (PrintWriter out = new PrintWriter(new FileWriter("gpa_records.txt", true))) {
        out.println("Name: " + name + ", ID: " + id);
        double totalQP = 0;
        double totCred = 0;

        for (int i = 0; i < tableModel.getRowCount(); i++) {
            String course = tableModel.getValueAt(i, 0).toString();
            double cred   = (double) tableModel.getValueAt(i, 1);
            double grade  = (double) tableModel.getValueAt(i, 2);
            
            out.println(course + "\t" + cred + "\t" + grade);
            totalQP += cred * grade;
            totCred  += cred;
        }
        double gpa;
        if (totCred == 0) {
            gpa = 0;
        } else {
            gpa = totalQP / totCred;
        }
        out.println("GPA: " + String.format("%.2f", gpa));
        out.println();

        JOptionPane.showMessageDialog(this, "Saved to your documents as gpa_records.txt\nYour GPA is: "  + String.format("%.2f", gpa),"Success",
            JOptionPane.INFORMATION_MESSAGE
        );

    } catch (IOException ex) {
        JOptionPane.showMessageDialog(this,"There was an error writing to file: " + ex.getMessage(),"File Error",
            JOptionPane.ERROR_MESSAGE
        );
    }
    }//GEN-LAST:event_btnSaveActionPerformed

