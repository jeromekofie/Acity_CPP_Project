    private void btnAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCourseActionPerformed
    if (tableModel.getRowCount() >= MAX_COURSES) {
        double totalGP = 0, totCred = 0;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            double cred  = (double) tableModel.getValueAt(i, 1);
            double grade = (double) tableModel.getValueAt(i, 2);
            totalGP += cred * grade;
            totCred  += cred;
        }
        double gpa = (totCred == 0) 
                ? 0 : totalGP / totCred;
        JOptionPane.showMessageDialog(this,
            tableModel.getRowCount() + " courses entered.\nYour GPA is: " +
              String.format("%.2f", gpa),
             "GPA Calculated",
            JOptionPane.INFORMATION_MESSAGE
        );
        btnAddCourse.setEnabled(false);
        return;
    }
    String course   = tfCourses.getText();
    String credStr  = tfCredit.getText();
    String gradeStr = tfGrades.getText();
    if (course.isEmpty() || credStr.isEmpty() || gradeStr.isEmpty()) {
        JOptionPane.showMessageDialog( this, "Please fill in all fields :(", "Input Error",
            JOptionPane.ERROR_MESSAGE
        );
        return;
    }
    double cred, grade;
    try {
        cred  = Double.parseDouble(credStr);
        grade = Double.parseDouble(gradeStr);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this,"Credit and Grade must be valid numbers.","Input Error",
            JOptionPane.ERROR_MESSAGE
        );
        return;
    }
    tableModel.addRow(new Object[]{ course, cred, grade });//this part adds or enters the rows into the table
    tfCourses.setText("");
    tfCredit .setText("");
    tfGrades .setText("");
    tfCourses.requestFocus();

    if (tableModel.getRowCount() == MAX_COURSES) { //this part ensures that when the 5th course is reached the gpa automatically shows 
        double totalGP = 0, totCred2 = 0;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            double c  = (double) tableModel.getValueAt(i, 1);
            double g2 = (double) tableModel.getValueAt(i, 2);
            totalGP += c * g2;
            totCred2 += c;
        }
        double gpa = (totCred2 == 0) ?
                0 : totalGP / totCred2;
        JOptionPane.showMessageDialog(this, "5 courses entered.\nYour GPA is: " + String.format("%.2f", gpa),"GPA successfully Calculated",
            JOptionPane.INFORMATION_MESSAGE
        );
        btnAddCourse.setEnabled(false);
    }
    }//GEN-LAST:event_btnAddCourseActionPerformed

