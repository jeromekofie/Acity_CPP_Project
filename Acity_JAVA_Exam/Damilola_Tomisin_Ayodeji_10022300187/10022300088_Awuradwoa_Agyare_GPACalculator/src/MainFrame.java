    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tableDetails.getSelectedRow();

        // Check if any row is selected
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a row to delete.", "No row selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Get the model of the table and remove the selected row
        DefaultTableModel model = (DefaultTableModel) tableDetails.getModel();
        model.removeRow(selectedRow);

    }//GEN-LAST:event_btnDeleteActionPerformed

