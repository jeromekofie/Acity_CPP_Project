    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String SName = tfSName.getText().trim();
        String SId = tfSID.getText().trim();
        String course = comboCourse.getSelectedItem().toString();
        String save = comboSave.getSelectedItem().toString();
        

        if (SName.isEmpty() || SId.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter your name and ID:");
            return;
        }

        try {
            String noSubStr = JOptionPane.showInputDialog("Enter the number of subjects for this course:");
            if (noSubStr == null) return;
            int noSubjects = Integer.parseInt(noSubStr);

            double totalGP = 0;
            double totalCH = 0;

            String allSubjects = "";

            for (int i = 1; i <= noSubjects; i++) {
                String gradeStr = JOptionPane.showInputDialog("Enter the grade (A, B+, B, C+, C, D, E, F) obtained for this subject " + i + ":");
                if (gradeStr == null) return;
                gradeStr = gradeStr.toUpperCase().trim();

                double gp = -1;
                if (gradeStr.equals("A")) {
                    gp = 4.0;
                } else if (gradeStr.equals("B+")) {
                    gp = 3.5;
                } else if (gradeStr.equals("B")) {
                    gp = 3.0;
                } else if (gradeStr.equals("C+")) {
                    gp = 2.5;
                } else if (gradeStr.equals("C")) {
                    gp = 2.0;
                } else if (gradeStr.equals("D")) {
                    gp = 1.5;
                } else if (gradeStr.equals("E")) {
                    gp = 1.0;
                } else if (gradeStr.equals("F")) {
                    gp = 0.0;
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid Grade! Try again. " + gradeStr);
                    return;
                }

                String creditStr = JOptionPane.showInputDialog("Enter the credit hours required for this subject " + i + ":");
                if (creditStr == null) return;
                double creditH = Double.parseDouble(creditStr);

                totalGP += gp * creditH;
                totalCH += creditH;

                allSubjects += "Subject " + i + ": Grade = " + gradeStr + ", Grade Point = " + gp + ", Credit Hours = " + creditH + "\n";
            }

            double gpa = totalGP / totalCH;

            String finalDisplay = "Gradebook" + "\n"
            + "............................." + "\n"
            + "Student Name: " + SName + "\n"
            + "Student ID: " + SId + "\n"
            + "Course: " + course + "\n"
            + allSubjects
            + "GPA: " + String.format("%.2f", gpa) + "\n";

            JOptionPane.showMessageDialog(null, finalDisplay);

            Object[] rowData = {
                SName,
                SId,
                course,
                totalCH,
                gpa,
            };

            DefaultTableModel model = (DefaultTableModel) tableDetails.getModel();
            model.addRow(rowData);
            
            if (save.equalsIgnoreCase("File")) {
                FileWriter records = new FileWriter("gpa_records.txt", true);
                records.write(finalDisplay);
                records.close();
            } else if (save.equalsIgnoreCase("Database")) {
                JOptionPane.showMessageDialog(null, "Database: Under Construction.");
            } else {
                JOptionPane.showMessageDialog(null, "Please select one of the two options given");
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error writing to file: " + ex.getMessage());
             

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Invalid! Please try again.");
        } 

    }//GEN-LAST:event_btnAddActionPerformed

