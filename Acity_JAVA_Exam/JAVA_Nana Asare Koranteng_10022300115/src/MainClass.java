    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    String name = tfName.getText();
    String studentId = tfID.getText();
    String save = comboSave.getSelectedItem().toString();

if (name.isEmpty() || studentId.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Please enter both Name and Student ID.");
    return;
}

try {
    String numSubjects = JOptionPane.showInputDialog("Enter number of courses:");
    if (numSubjects == null) return;
    int numCourse = Integer.parseInt(numSubjects);

    double sumGradePointsTimesCredits = 0; 
    double sumCreditHours = 0;             

    String allSubjects = "";

    for (int i = 1; i <= numCourse; i++) {
        String nameCourse = JOptionPane.showInputDialog("Enter name of Course:");
        String grade = JOptionPane.showInputDialog("Enter letter grade (A, B+, B, C+, C, D, E, F) for: " + nameCourse + ":");
        if (grade == null) return;
        grade = grade.toUpperCase();

        double gradePoint = -1;
        switch (grade) {
            case "A":
                gradePoint = 4.0;
                break;
            case "B+":
                gradePoint = 3.5;
                break;
            case "B":
                gradePoint = 3.0;
                break;
            case "C+":
                gradePoint = 2.5;
                break;
            case "C":
                gradePoint = 2.0;
                break;
            case "D":
                gradePoint = 1.5;
                break;
            case "E":
                gradePoint = 1.0;
                break;
            case "F":
                gradePoint = 0.0;
                break;
            default:
                JOptionPane.showMessageDialog(null, "Invalid grade entered: " + grade);
                return;
        }

        String creditStr = JOptionPane.showInputDialog("Enter credit hours for: " + nameCourse + ":");
        if (creditStr == null) return;
        double credit = Double.parseDouble(creditStr);

     
        sumGradePointsTimesCredits += gradePoint * credit;
        sumCreditHours += credit;

        allSubjects +=nameCourse + ": Grade = " + grade + "     |        Grade Point = " + gradePoint + "       |        Credit Hours = " + credit + "\n";
    }

    double gpa = sumGradePointsTimesCredits / sumCreditHours;

    String finalOutput = "Name: " + name + "\n"
            + "ID: " + studentId + "\n"
            + allSubjects
            + "GPA: " + String.format("%.2f", gpa) + "\n"
            + "--------------------------------------------------\n";

    JOptionPane.showMessageDialog(null, finalOutput);

    Object[] rowData = {
            name,
            studentId,
            gpa,
    };

    DefaultTableModel model = (DefaultTableModel) tableStudents.getModel();
    model.addRow(rowData);

    if (save.equalsIgnoreCase("File")) {
        FileWriter writer = new FileWriter("gpa_records.txt", true);
        writer.write(finalOutput);
        writer.close();
    } else if (save.equalsIgnoreCase("Database")) {
        JOptionPane.showMessageDialog(null, "Database: Under Construction.");
    }

} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(null, "Invalid number entered. Please try again.");
} catch (IOException ex) {
    JOptionPane.showMessageDialog(null, "Error writing to file: " + ex.getMessage());
}
    }//GEN-LAST:event_btnAddActionPerformed

