    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed

 String name = tfName.getText().trim();
    String studentId = tfID.getText().trim();
    String course = comboCourse.getSelectedItem().toString();
    String save = comboSave.getSelectedItem().toString();

if (name.isEmpty() || studentId.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Please enter both your Name and Student ID.");
    return;
}

try {
    String numSubStr = JOptionPane.showInputDialog("Enter number of subjects:");
    if (numSubStr == null) return;
    int numSubjects = Integer.parseInt(numSubStr);

    double totalGradePoints = 0;
    double totalCreditHours = 0;

    String allSubjects = "";

    for (int i = 1; i <= numSubjects; i++) {
        String gradeStr = JOptionPane.showInputDialog("Enter letter grade (A, B+, B, C+, C, D, E, F) for subject " + i + ":");
        if (gradeStr == null) return;
        gradeStr = gradeStr.toUpperCase().trim();

        double gradePoint = -1;
        if (gradeStr.equals("A")) {
            gradePoint = 4.0;
        } else if (gradeStr.equals("B+")) {
            gradePoint = 3.5;
        } else if (gradeStr.equals("B")) {
            gradePoint = 3.0;
        } else if (gradeStr.equals("C+")) {
            gradePoint = 2.5;
        } else if (gradeStr.equals("C")) {
            gradePoint = 2.0;
        } else if (gradeStr.equals("D")) {
            gradePoint = 1.5;
        } else if (gradeStr.equals("E")) {
            gradePoint = 1.0;
        } else if (gradeStr.equals("F")) {
            gradePoint = 0.0;
        } else {
            JOptionPane.showMessageDialog(null, "Invalid grade entered: " + gradeStr);
            return;
        }

        String creditStr = JOptionPane.showInputDialog("Enter credit hours for subject " + i + ":");
        if (creditStr == null) return;
        double credit = Double.parseDouble(creditStr);

        totalGradePoints += gradePoint * credit;
        totalCreditHours += credit;

        allSubjects += "Subject " + i + ": Grade = " + gradeStr + ", Grade Point = " + gradePoint + ", Credit Hours = " + credit + "\n";
    }

    double gpa = totalGradePoints / totalCreditHours;

    String finalOutput = String.format(
    "Name: %s\nID: %s\nCourse: %s\n%sGPA: %.2f\n",
    name, studentId, course, allSubjects.toString(), gpa
);


    JOptionPane.showMessageDialog(null, finalOutput);

    Object[] rowData = {
            name,
            studentId,
            course,
            totalCreditHours,
            gpa,
    };

    DefaultTableModel model = (DefaultTableModel) tableStudents.getModel();
    model.addRow(rowData);

    if (save.equalsIgnoreCase("File")) {
        FileWriter writer = new FileWriter("StudentRecords.txt", true);
        writer.write(finalOutput);
        writer.close();
    } else if (save.equalsIgnoreCase("Database")) {
        JOptionPane.showMessageDialog(null, "Database: yet to be implemented.");
    } else {
        JOptionPane.showMessageDialog(null, "Unknown save option selected.");
    }

} catch (NumberFormatException ex) {
    JOptionPane.showMessageDialog(null, "Invalid number entered. Please try again.");
} catch (IOException ex) {
    JOptionPane.showMessageDialog(null, "Error writing to file: " + ex.getMessage());
}


    }//GEN-LAST:event_btnNextActionPerformed

