    // Assigning the grade points to the grades 
    private double getGradePoint(String grade) {
        return switch (grade) {
            case "A" -> 4.0;
            case "B+" -> 3.5;
            case "B" -> 3.0;
            case "C+" -> 2.5;
            case "C" -> 2.0;
            case "D" -> 1.5;
            case "E" -> 1.0;
            case "F" -> 0.0;
            default -> 0.0;
        };
    }
    
    private void saveToDatabase() throws SQLException {
        String name = txtStudentName.getText();
        String id = txtStudentID.getText();
        double gpa = Double.parseDouble(lblGPA.getText());

        DefaultTableModel model = (DefaultTableModel) tableCourses.getModel();
        int rowCount = model.getRowCount();

        try (Connection conn = DBConnection.getConnection()) {
            String query = "INSERT INTO students (id, name, course, hours, grade, point, gpa) VALUES (?, ?, ?, ?, ?, ?, ?)";
            try (PreparedStatement stmt = conn.prepareStatement(query)) {
                for (int i = 0; i < rowCount; i++) {
                    String course = model.getValueAt(i, 0).toString();
                    double credit = Double.parseDouble(model.getValueAt(i, 1).toString());
                    String grade = model.getValueAt(i, 2).toString();
                    double point = Double.parseDouble(model.getValueAt(i, 3).toString());
                    
                    stmt.setString(1, id);
                    stmt.setString(2, name);
                    stmt.setString(3, course);
                    stmt.setDouble(4, credit);
                    stmt.setString(5, grade);
                    stmt.setDouble(6, point);
                    stmt.setDouble(7, gpa);
                    
                    stmt.addBatch();
                }
                
                stmt.executeBatch();
            }
        }
}

