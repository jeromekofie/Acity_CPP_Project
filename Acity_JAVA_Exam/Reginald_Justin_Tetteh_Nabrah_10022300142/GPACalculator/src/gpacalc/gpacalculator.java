    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableCourses.getModel();

    double totalPoints = 0;
    double totalCredits = 0;

    for (int i = 0; i < model.getRowCount(); i++) {
        double credit = Double.parseDouble(model.getValueAt(i, 1).toString());
        double gradePoint = Double.parseDouble(model.getValueAt(i, 3).toString());

        totalPoints += gradePoint * credit;
        totalCredits += credit;
    }

    if (totalCredits == 0) {
        JOptionPane.showMessageDialog(this, "No valid courses to calculate GPA.", "Calculation Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    double gpa = totalPoints / totalCredits;
    lblGPA.setText(String.format("%.2f", gpa));
    lblMessage.setText("GPA calculated successfully!");
    }//GEN-LAST:event_calculateButtonActionPerformed

    private void refreshButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tableCourses.getModel();
    model.setRowCount(0); // Clear existing rows

    try (Connection conn = DBConnection.getConnection();
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT * FROM student")) {

        while (rs.next()) {
            String course = rs.getString("course");
            int hours = rs.getInt("hours");
            String grade = rs.getString("grade");
            double point = rs.getDouble("point");

            model.addRow(new Object[]{course, hours, grade, point});
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error loading data: " + ex.getMessage());
    }
    }//GEN-LAST:event_refreshButtonActionPerformed

