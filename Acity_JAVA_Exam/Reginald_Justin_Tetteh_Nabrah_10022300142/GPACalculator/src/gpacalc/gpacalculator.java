    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
                int selectedRow = tableCourses.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Select a row to edit.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String course = txtCourse.getText();
    String creditStr = txtCreditHours.getText();
    String grade = comboGrade.getSelectedItem().toString().trim();

    if (course.isEmpty() || creditStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Fill in Course and Credit Hours to update.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    double credit;
    try {
        credit = Double.parseDouble(creditStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Credit Hours must be numeric.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        return;
    }

    double point = getGradePoint(grade);

    DefaultTableModel model = (DefaultTableModel) tableCourses.getModel();
    model.setValueAt(course, selectedRow, 0);
    model.setValueAt(credit, selectedRow, 1);
    model.setValueAt(grade, selectedRow, 2);
    model.setValueAt(point, selectedRow, 3);

    JOptionPane.showMessageDialog(this, "Course updated successfully!");
    }//GEN-LAST:event_editButtonActionPerformed

