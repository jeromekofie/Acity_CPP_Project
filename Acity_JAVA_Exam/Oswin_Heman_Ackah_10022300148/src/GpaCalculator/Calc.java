    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        try (FileWriter writer = new FileWriter("gpa_records.txt")) {
            writer.write("Student Name: " + txtName.getText() + "\n");
            writer.write("Student ID: " + txtID.getText() + "\n");
                
            for (Course c : courses) {
            writer.write(c.name + ", " + c.creditHours + " hrs, Grade: " + c.grade + "\n");
            }
            Object[] gpa = null;
//            writer.write("GPA: " + cmbGrade.getSelectedItem().toString() + "\n");
             writer.write("Your GPA is: " + gpa); // assuming `gpa` is a double/float/String variable

            JOptionPane.showMessageDialog(this, "Saved to file!");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error saving file.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

