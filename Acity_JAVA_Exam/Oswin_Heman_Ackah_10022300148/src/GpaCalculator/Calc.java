    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        double totalPoints = 0;
        int totalCredits = 0;

        for (Course c : courses) {
            totalPoints += c.getGradePoint() * c.creditHours;
            totalCredits += c.creditHours;
        }

        double gpa = totalCredits > 0 ? totalPoints / totalCredits : 0;
        JOptionPane.showMessageDialog(this, "GPA: " + String.format("%.2f", gpa));
    }//GEN-LAST:event_btnCalculateActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String courseName = txtCourse.getText();
        String creditText = txtCreditHrs.getText();
        String grade = cmbGrade.getSelectedItem().toString();

        if (courseName.isEmpty() || creditText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required!");
            return;
        }

        int creditHrs = Integer.parseInt(creditText);
        Course course = new Course(courseName, creditHrs, grade);
        courses.add(course);

        // Append to JTextArea
        txtCourseList.append(courseName + " | " + creditHrs + " credit hours | Grade: " + grade + "\n");

        // Clear input fields
        txtCourse.setText("");
        txtCreditHrs.setText("");
        cmbGrade.setSelectedIndex(0);

    }//GEN-LAST:event_btnAddActionPerformed

