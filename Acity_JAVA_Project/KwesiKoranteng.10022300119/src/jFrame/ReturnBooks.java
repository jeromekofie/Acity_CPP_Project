    private void rSMaterialButtonCircle8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSMaterialButtonCircle8ActionPerformed

// Step 1: Get data from the text fields
    String studentName = txt_studentName.getText();
    String bookIdText = txt_bookId.getText();
    String dueDateText = txt_dueDate.getText();
    String returnDateText = txt_returnDate.getText();

    // Validate user inputs
    if (studentName.isEmpty() || bookIdText.isEmpty() || dueDateText.isEmpty() || returnDateText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields must be filled!", "Input Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method if input is incomplete
    }

    try {
        // Step 2: Convert bookId to an integer
        int bookId = Integer.parseInt(bookIdText);

        // Step 3: Format the dates from the text fields
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date dueDate = sdf.parse(dueDateText);
        Date returnDate = sdf.parse(returnDateText);

        // Step 4: Calculate the fine
        double fineAmount = calculateFine(dueDate, returnDate);

        // Step 5: Insert data into the database
        insertFine(studentName, bookId, dueDateText, returnDateText, fineAmount);

        // Step 6: Refresh the fines table with the latest data
        setFineDetailsToTable();

        // Step 7: Optionally, clear the input fields
        txt_studentName.setText("");
        txt_bookId.setText("");
        txt_dueDate.setText("");
        txt_returnDate.setText("");

    } catch (Exception e) {
        // Catch any exceptions that occur during the process
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error processing the data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_rSMaterialButtonCircle8ActionPerformed

