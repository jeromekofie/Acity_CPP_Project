    // Method to calculate fine based on due and return dates
    private double calculateFine(Date due, Date ret) {
        long difference = ret.getTime() - due.getTime();
        long daysLate = difference / (1000 * 60 * 60 * 24);  // Convert time difference to days
        double finePerDay = 1.0;  // Fine per day for late return

        if (daysLate > 0) {
            return daysLate * finePerDay;
        } else {
            return 0.0;  // No fine if the book is returned on or before the due date
        }
    }

    // Method to insert fine into the fine_details table
    public void insertFine(String studentName, int bookId, String dueDate, String returnDate, double fineAmount) {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_ms", "root", "kwesi123")) {
            String sql = "INSERT INTO fine_details(student_name, book_id, due_date, return_date, fine_amount) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, studentName);
            pst.setInt(2, bookId);
            pst.setString(3, dueDate);
            pst.setString(4, returnDate);
            pst.setDouble(5, fineAmount);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Fine inserted successfully!");
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    // Method to display fines in the fines table
    public void setFineDetailsToTable() {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library_ms", "root", "kwesi123");
             Statement pst = con.createStatement()) {

            ResultSet rs = pst.executeQuery("SELECT * FROM fine_details");

            DefaultTableModel model = (DefaultTableModel) tbl_fines.getModel();
            model.setRowCount(0);  // Clear existing data

            while (rs.next()) {
                Object[] row = {
                    rs.getString("student_name"),
                    rs.getInt("book_id"),
                    rs.getDate("due_date"),
                    rs.getDate("return_date"),
                    rs.getDouble("fine_amount")
                };
                model.addRow(row);
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    // Method to calculate and display fine for a book on return
    public void handleReturn() {
        try {
            String studentName = txt_studentName.getText();
            int bookId = Integer.parseInt(txt_bookId.getText());
            String dueDateString = txt_dueDate.getText();
            String returnDateString = txt_returnDate.getText();

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date dueDate = sdf.parse(dueDateString);
            Date returnDate = sdf.parse(returnDateString);

            // Calculate fine
            double fineAmount = calculateFine(dueDate, returnDate);

            // Insert fine into the database
            insertFine(studentName, bookId, dueDateString, returnDateString, fineAmount);

            // Refresh the fines table
            setFineDetailsToTable();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error processing the return.");
        }
    }

  
